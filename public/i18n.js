let currentLang = localStorage.getItem('mudream_lang') || 'pt';

function t(key, vars) {
    let val = TRANSLATIONS[currentLang]?.[key] || TRANSLATIONS.pt[key] || key;
    if (vars && typeof val === 'string') {
        for (const k in vars) val = val.split('{' + k + '}').join(vars[k]);
    }
    return val;
}

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('mudream_lang', lang);
    applyTranslations();
}

function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.dataset.i18n;
        const val = t(key);
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
            el.placeholder = val;
        } else if (el.tagName === 'OPTION') {
            el.textContent = val;
        } else if (el.dataset.i18nAttr === 'title') {
            el.title = val;
        } else if (el.dataset.i18nHtml) {
            el.innerHTML = val;
        } else {
            el.textContent = val;
        }
    });

    const suggestions = document.querySelectorAll('.suggestion-btn');
    const suggTexts = t('chatSuggestions');
    suggestions.forEach((btn, i) => {
        if (suggTexts[i]) {
            btn.textContent = suggTexts[i];
            btn.dataset.q = suggTexts[i];
        }
    });

    if (typeof updateAlarmInfoText === 'function') {
        updateAlarmInfoText();
    }

    const langSelect = document.getElementById('langSelect');
    if (langSelect) langSelect.value = currentLang;
}
